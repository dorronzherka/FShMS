    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(emailTextField.getText().equals("") || emailTextField == null && nrTelTextField.getText().equals("") || nrTelTextField == null && jComboBox2.getSelectedIndex() < 0)
                 JOptionPane.showMessageDialog(this, "Asnjë nga opcionet si keni mbushur!","Gabim!",JOptionPane.ERROR_MESSAGE);
            else if(jComboBox2.getSelectedIndex() < 0)
                 JOptionPane.showMessageDialog(this, "Nuk e keni zgjedhur klientin!","Gabim!",JOptionPane.ERROR_MESSAGE);
            else if(emailTextField.getText().equals("") || emailTextField == null)
                 JOptionPane.showMessageDialog(this, "Nuk e keni shtypur email!","Gabim!",JOptionPane.ERROR_MESSAGE);
            else if(nrTelTextField.getText().equals("") || nrTelTextField == null)
                JOptionPane.showMessageDialog(this, "Nuk e keni shtypur numrin e telefonit!","Gabim!",JOptionPane.ERROR_MESSAGE);
            else{
                KontaktiKlienti kk = new KontaktiKlienti();
                kk.setTel(nrTelTextField.getText());
                kk.setEmail(emailTextField.getText());
                Klienti k = (Klienti)jComboBox2.getSelectedItem();
                kk.setIDKlienti(k);
                kkR.create(kk);
                JOptionPane.showMessageDialog(this, "U regjistrua me sukses!");
            }
        }catch(KontaktiKlientiException ke){
            JOptionPane.showMessageDialog(this, ke.getMessage(),"Gabim!",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


