    public Klienti[] convertToArray(){
       int i = 0;
       List<Klienti> l = kR.findAll();
       Iterator<Klienti> li = l.iterator();
       Klienti[] list = new Klienti[l.size()];
       while(li.hasNext()){
           if(i < l.size())
               list[i++] = li.next();
       }
       return list;
  }
